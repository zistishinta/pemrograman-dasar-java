package demo4_classmath;

public class nilaiAbsolut {

    public static void main(String[] args) {
        // nilai absolut adalah semua nilainya positif 
        int a = -2;
        int b = 7;
        
        System.out.println("NilaiAbsolute a : "+Math.abs(a));
        System.out.println("NilaiAbsolute b : "+Math.abs(b)); 
    }
}
